<pre>■第1部　CodeIgniterによるWeb開発</pre>
<pre>CHAPTER 1	CodeIgniterとは

1.1 PHPとCodeIgniterフレームワーク
	1.1.1 PHP言語の特徴とフレームワークとの親和性
	1.1.2 フレームワークを使う利点
		作業工数の削減とコストの低減
		ビジネスロジックの作り込みに専念可能
		グループによる共同作業の促進
		不用意なセキュリティホールの回避
1.2 CodeIgniterの特徴
	1.2.1 CodeIgniterの10の利点
		①少ないメモリとディスクで動く
		②高いパフォーマンス
		③PHP4/5対応（Webホスティングとの幅広い互換性）
		④設定ファイルをほとんど触ることなく動く
		⑤コマンドラインによる操作を必要としない
		⑥コーディング規約を硬く守ることを強要しない
		⑦PEARのような大規模で一枚岩な構造のライブラリを必要としない
		⑧テンプレート言語の学習が必要ない
		⑨シンプルな機能構成
		⑩明確で完全なドキュメント（仕様書）
	1.2.2 CodeIgniterの設計思想
		コンポーネントの低い結合度と高い凝集度
		動的なインスタンス化と疎結合
	1.2.3 ライセンス</pre>
<pre>CHAPTER 2	開発環境の構築

2.1 CodeIgniterでの開発に必要なソフトウェア
2.2 XAMPP、Eclipse/PDT、Xdebug
	2.2.1 XAMPP
